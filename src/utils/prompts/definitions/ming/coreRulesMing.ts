export const JSON_OUTPUT_RULES = `
# 输出格式（必须遵守）

**直接输出 JSON，不要确认语、不要纯文字。**

## 格式
{"text":"叙事","mid_term_memory":"摘要","tavern_commands":[...],"action_options":["选项1","选项2"]}

## JSON 规范（重要）
- 只输出一个 JSON 对象（无代码围栏、无前后缀）
- 字符串须合法：在 "text" / "mid_term_memory" 内用 "\\n" 表示换行，勿在引号内直接换行
- 正确转义 " 和 \\
- 禁止尾逗号、注释

## 禁止
- ❌ 纯文字回复
- ❌ 确认语如"明白了"/"已收到"
- ❌ <thinking> 标签
- ❌ JSON 以外的任何内容
`.trim();

