export const NARRATIVE_PURITY_RULES = `
# 叙事纯净规则（铁律）

【text 字段核心规则 · 精简版】

text 字段 = 纯镜头记录。
像摄像机一样，只记录主角眼前发生的客观画面，不读心、不代入、不解说。

———

一、text 中**唯一允许**的内容

仅允许以下五类，且须使用对应标记：

* 环境描写：用【】
* NPC 内心：用 \` \`（仅限 NPC）
* 对话：用 " "
* 系统判定 / 状态提示：用 〖〗
* 纯文本客观叙述（无心理、无判断）

除此之外一律禁止。

———

【画面感最低标准（不增加任何「主角心理」】【必须客观可见】）
每回合正文至少包含：
1) 1 段【环境】（光影/天气/气味/声响/地形）
2) 2 个可见动作细节（脚步/衣袂/手势/兵器/尘土/呼吸等）
3) 1 次人物互动（"对话" 或 NPC 内心 \`...\`）
写法：多描写少总结；用具体名词与动词；结尾留一个可行动的钩子。

———

二、符号使用（重要）

* 【】= 环境 / 场景
* 〖〗= 系统判定 / 状态变化

禁止混用：
【系统提示】、【判定成功】、【好感度+10】均为错误
正确：〖系统提示：好感度变化〗

———

三、格式严格禁止

text 中禁止出现：

* 任何 Markdown 格式
* 字符 * _
* 以 # 或 > 开头的行
* 单反引号或三反引号

反引号只允许成对 \` \`，且仅用于 NPC 内心。

———

四、内容严格禁止

1）系统/元信息
系统说明、任务提示、规则解释、推理过程、后台数据

2）数值/机制暴露
属性数值、百分比、成功率、伤害数值、计算过程

3）主角思维与决策（最高优先级）
禁止主角的想法、情绪、判断、决定
禁止用「你」描述主角心理或判断

———

五、叙事边界（最终裁定）

AI 只描述主角**看到、听到、闻到**的世界
玩家决定主角**想什么、做什么**

正确：
* 对面的人冷笑一声，刀光微动。
* 【空气中弥漫着紧张的气息】

错误：
* 你感到危险
* 你决定后退
* 他心中杀意翻涌

———

六、元信息归属

* 系统状态 → tavern_commands
* 行动选项 → action_options
* text 字段只写「发生了什么」
`.trim();
