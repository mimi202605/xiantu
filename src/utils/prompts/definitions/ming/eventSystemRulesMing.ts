export const EVENT_SYSTEM_RULES_MING = `
# 规则：世界事件（重要）

## 核心理念
- 世界不以玩家为中心：势力、资源、人心都会自行演变
- 大事件须带来可感知后果：危险、资源、关系、局势、处境变化等至少一项
- 事件须「刚刚发生」：用现场感呈现，而非公告式总结

## 事件类型示例（可混用、可按世界观调整）
- 势力冲突（围剿、结盟、割据、火并）
- 局势变化（天象异动、资源枯竭、规则松动、封印破解）
- 重大发现（宝物出世、遗迹显露、传承现身、秘密曝光）
- 人物风波（友方受伤、友方晋升、友方遭追杀、恩怨升级）

## 合理性
- 事件强度须与玩家处境、声望、位置、状态相符
- 涉及好友/NPC 时，须参考存档中的关系与好感推断动机与结局
- 事件后续须留下悬念或后续，推动玩家行动

---

## 事件日志记录规则

### 核心原则
大事件/关键事件须通过 tavern_commands 添加到「社交.事件.事件记录」，作为世界历史永久保存。

### 建议记录的事件类型
- **处境转折**：主角或重要 NPC 的重大突破、重伤、死亡
- **关系转折**：结为伴侣/师徒/结拜、决裂、重要人物陨落
- **世界类**：势力冲突、局势变化、重大发现、人物风波等随机或系统触发的大事件

### 可不记录
- 日常互动、普通物品获取、普通社交、小规模冲突

### 事件记录格式（通过 tavern_commands 添加）
{
  "action": "push",
  "key": "社交.事件.事件记录",
  "value": {
    "事件ID": "evt_[类型]_[时间戳]",
    "事件名称": "简短标题",
    "事件类型": "势力冲突/局势变化/重大发现/人物风波 等",
    "事件描述": "详细描述",
    "影响等级": "轻微/中等/重大/灾难",
    "影响范围": "影响范围描述",
    "相关人物": ["涉及的人物名"],
    "事件来源": "随机/玩家影响/系统",
    "发生时间": { "年": x, "月": x, "日": x, "小时": x, "分钟": x }
  }
}
`.trim();
