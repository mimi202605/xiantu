export const TEXT_FORMAT_MARKERS = `
# 文本格式标记

## 允许的格式标记（仅限以下）
- 环境描写: \`【…】\`
- 内心思考: \`\`…\`\`
- 角色对话: \`"…"\`
- 系统判定/系统提示: \`〖…〗\`
- 除此以外，全文须为纯文本，不使用任何 Markdown 格式

## 符号使用规则（重要）
**【】与〖〗的区别：**
- **【】** = 环境描写
  - 用于场景、氛围、环境变化
  - 例：【前方传来阵阵杀气】【天色渐暗，乌云密布】

- **〖〗** = 系统判定/系统提示
  - 用于判定结果、系统提示、状态变化、好感度变化
  - 例：〖探索:成功,判定值:45,难度:35〗
  - 例：〖系统提示：获得状态「精力充沛」，持续2小时〗
  - 例：〖系统提示：好感度+10（当前60：友好）〗

### 常见错误
- 【系统提示：…】 ← 错误，系统提示应用〖〗
- 【探索:成功…】 ← 错误，判定应用〖〗
- 〖前方传来杀气〗 ← 错误，环境描写应用【】

### 正确示例
- 【前方传来阵阵杀气】 ← 环境用【】
- 〖探索:成功,判定值:45,难度:35〗 ← 判定用〖〗

## 严格禁止的格式
- 禁止 Markdown：不要输出粗体/斜体/下划线/标题/列表/引用/代码块
- 禁止 * 与 _
- 禁止以 # 或 > 开头的行
- 禁止单反引号、三反引号；仅内心思考可用成对双反引号 \`\`…\`\`

## 重要提醒
如需强调词语，直接写出，不要用任何 Markdown 或符号强调。
`.trim();

export const DICE_ROLLING_RULES = `
# 判定系统

## 判定格式（必须遵守）

\`\`\`
〖类型:结果,判定值:X,难度:Y,基础:B,幸运:L,环境:E,状态:S〗
\`\`\`

**注意：用〖〗不是【】**

### 正确示例
〖探索:成功,判定值:45,难度:35,基础:30,幸运:+8,环境:+5,状态:+2〗
〖社交:大成功,判定值:68,难度:50,基础:52,幸运:+12,环境:+4,状态:0〗
〖行动:失败,判定值:42,难度:55,基础:38,幸运:-3,环境:+7,状态:0〗
〖危机:大失败,判定值:18,难度:60,基础:25,幸运:-5,环境:0,状态:-2〗

### 错误示例
❌ 【探索:成功…】 ← 括号错误
❌ 〖判定:成功〗 ← 缺数值字段

### 字段参考
| 字段 | 说明 | 值域 |
|------|------|------|
| 类型 | 判定类型 | 探索/社交/行动/危机/躲避/感知等 |
| 结果 | 判定结果 | 大失败/失败/成功/大成功/完美 |
| 判定值 | 最终值 | 数字 |
| 难度 | 目标值 | 10/20/35/50/70/90 |
| 基础 | 属性与状态 | 数字 |
| 幸运 | 运气波动 | +X 或 -X |
| 环境 | 环境修正 | +X 或 0 |
| 状态 | 状态修正 | +X 或 -X |

---

## 反媚俗规则（最高优先级）

结果须公正，不偏袒主角。

### 绝对禁止
1. **禁止偏袒**：换作路人甲结果应相同
2. **禁止轻描淡写**：大失败=重罚，不是「差点成功」
3. **禁止主角光环**：对手不会突然变蠢
4. **禁止巧合救场**：不要「恰好」「正巧」解围
5. **禁止编造加成**：只使用存档数据

---

## 公式与难度

\`\`\`
最终判定值 = 基础 + 幸运 + 环境 + 状态
\`\`\`

**难度**：极易 10 | 简单 20 | 普通 35 | 困难 50 | 艰难 70 | 极难 90

| 结果 | 条件 | 后果 |
|------|------|------|
| 大失败 | <难度-15 | 重伤/重大损失/关系恶化 |
| 失败 | <难度 | 轻伤/部分损失/无进展/得罪对方 |
| 成功 | ≥难度 | 达成目的 |
| 大成功 | ≥难度+15 | 额外奖励/重创对手/品质提升/好感大增 |
| 完美 | ≥难度+30 | 重大转机/关键突破/结为盟友 |

---

## 何时判定

**必须判定**：冲突攻防、危险探索、社交博弈、躲避逃跑、关键行动
**无需判定**：日常行为、纯叙事对话
`.trim();

