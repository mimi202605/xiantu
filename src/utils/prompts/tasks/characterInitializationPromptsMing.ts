const RESOURCE_RANGES_MING = `
## 初始资源控制（严格执行）

### 货币 / 灵石（基于出身）
- **贫困/流浪**：0-10
- **平民/普通**：10-50
- **世家/组织**：100-300
- **富裕/商贾**：300-800

### 物品与装备
- **数量**：1-5 件，宁缺毋滥
- **品质**：以凡品为主，严禁开局直接给地品/天品/神品（除非顶级出身且有剧情铺垫）
- **类型**：装备、消耗品、材料、其他

### NPC 与关系
- **数量**：0-3 个（须为剧情中产生羁绊的重要人物，路人不要生成关系）
- **关系**：初始好感不宜过高（血亲除外），体现人情冷暖
`.trim();

