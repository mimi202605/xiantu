const COMMANDS_RULES_MING = `
## 初始化命令（tavern_commands）

### 必须执行的命令（按顺序）：

1. **时间** - 设置 \`元数据.时间\`
   - 格式：{"年":1050,"月":1,"日":1,"小时":8,"分钟":0}
   - 同时设置 \`角色.身份.出生日期\`（出生年 = 元数据.时间.年 - 开局年龄）

2. **位置** - 设置 \`角色.位置\`
   - 必须包含 {描述, x, y}
   - 描述格式：大陆·区域·地点（用·分隔）
   - 从可用地点列表中选择，坐标 x、y 范围 0-10000

3. **声望** - 设置 \`角色.属性.声望\`
   - 普通出身：0-10
   - 有势力/组织背景：10-50
   - 名门/显赫：50-100

4. **随机项** - 若出身/天赋为「随机」
   - 用 \`set\` 写入 \`角色.身份.出身\` 或 \`角色.身份.天赋\` 的具体内容
   - 出身格式：{"名称":"…","描述":"…"} 或 string

5. **资源** - 设置初始资源
   - \`角色.背包.灵石\`（根据出身决定数量，见下方资源控制）
   - \`角色.背包.物品.{物品ID}\`（如有初始物品，类型为 装备/消耗品/材料/其他）

6. **NPC** - 仅创建文本中明确提到的重要人物（0-3 个）
   - 写入 \`社交.关系.{NPC 中文名}\`
   - key 使用 NPC 的中文名，NPC 对象内的 \`名字\` 与 key 一致

7. **NSFW：玩家身体**（仅酒馆端）
   - 若 系统配置.nsfwMode=true，必须初始化 \`角色.身体\` 或 \`角色.身体部位开发\`
   - 用 set 写入合理对象，严禁「待 AI 生成」占位

### 约束：
- 所有 key 以 元数据/角色/社交/世界/系统 开头
- 开局只用 set，不使用 add/push/delete
`.trim();

